                                        <p><strong>CEO của thương hiệu Eunoia by AN – Vũ Lan Anh là người
                                                dành nhiều tâm huyết cho công tác thiện nguyện.</strong></p>
                                        <p><br>Eunoia
                                            by AN là thương hiệu thời trang trẻ em của CEO Vũ Lan Anh. Eunoia by
                                            AN (tạm dịch: ý nghĩ thuần khiết của An) ra đời giống như một món
                                            quà của chị Vũ Lan Anh dành tặng cho con gái của mình. Với slogan
                                            “Unique as a child: Every child is a unique child and Unique as a
                                            child”, Eunoia by AN muốn gửi gắm thông điệp về sự khát khao khám
                                            phá vẻ đẹp độc đáo, cá tính riêng tiềm ẩn bên trong mỗi tâm hồn trẻ
                                            thơ.</p>
                                        <p>CEO Vũ Lan Anh gửi gắm vào thương hiệu Eunoia by AN tình
                                            yêu trẻ em và tâm huyết với sự nghiệp ươm mầm tài năng nhí. Chị cùng
                                            Eunoia by AN đã lan tỏa tình yêu thương với loạt dự án thiện nguyện
                                            ý nghĩa.</p>
                                        <p><em>“Mỗi đứa trẻ đều là một phiên bản đặc biệt. Tuy
                                                ước ao và khát khao khác nhau, hoàn cảnh khác nhau nhưng các con
                                                đều xứng đáng được yêu thương và được bình đẳng hoá giấc mơ. Thiện
                                                nguyện – ươm mầm tài năng cho trẻ nhỏ trong hoàn cảnh khó khăn là
                                                tâm nguyện của tôi khi bước chân vào hành trình này”</em>, CEO Vũ
                                            Lan Anh tâm sự.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/workshop_nha_thiet_ke_nhi_0d_2_3e18599e05984dbe838ad76585a66703_1024x1024.jpg')}}">
                                        </p>
                                        <p style="text-align: center;">Dự án truyền cảm hứng nhà thiết kế nhí 0
                                            đồng tại TP Ninh Bình do thương hiệu Eunoia by AN phối hợp với Ninh
                                            Bình Legend Hotel tổ chức.</p>
                                        <p>Mới đây, thương hiệu Eunoia by AN
                                            đã kết hợp với khách sạn 5 sao Ninh Bình Legend Hotel tổ chức buổi
                                            workshop nằm trong dự án truyền cảm hứng nhà thiết kế nhí 0 đồng tại
                                            TP Ninh Bình. Tại đây, các bé được tìm hiểu về ngành thiết kế thời
                                            trang, làm quen với các loại vải, cách may vá, cách lựa chọn phụ
                                            kiện đi kèm và thỏa sức sáng tạo, tự tay thiết kế một bộ trang phục
                                            cho riêng mình.</p>
                                        <p>Bên cạnh đó, với mong muốn nối dài vòng tay
                                            yêu thương, ấp ôm những mảnh đời khó khăn, CEO Vũ Lan Anh đã đến
                                            thăm và tặng quà cho các bé bị bỏ rơi đang được nuôi dưỡng tại Bệnh
                                            viện nhi Trung ương. Trước đó, mọi hoạt động từ thiện suốt 8 năm qua
                                            của chị đều xoay quanh trẻ em. Như nấu cháo cho bệnh nhi tại Bệnh
                                            viện Châm cứu Trung ương, tổ chức hai chuyến thiện nguyện mang giáo
                                            cụ học tập cho học sinh ở vùng núi Điện Biên, Lào Cai, kêu gọi đóng
                                            góp mua cặp phao cứu sinh cho học sinh Quảng Bình mùa lũ…</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/ceo_vu_lan_anh_7_31acc5922acd417ba5dffdf56eda98a8.jpg')}}">
                                        </p>
                                        <p style="text-align: center;">CEO Vũ Lan Anh trao quà cho các bé bị bỏ
                                            rơi đang được nuôi dưỡng tại Bệnh viện nhi Trung ương.</p>
                                        <p>Đặc
                                            biệt, CEO Vũ Lan Anh luôn ấp ủ thực hiện những show diễn dành cho
                                            các em nhỏ có hoàn cảnh khó khăn được hiện thực giấc mơ được sải
                                            bước trên sàn catwalk và biến thành công chúa trong những bộ váy
                                            lộng lẫy của thương hiệu Eunoia by AN. Chị mong muốn đem đến những
                                            điều giản dị nhất nhưng trân quý nhất trao cho con gái cũng như
                                            những những bạn nhỏ đam mê thời trang.</p>
                                        <p>Trong show diễn thời
                                            trang mới đây diễn ra tại Phú Thọ, BST “Cổ tích” của Eunoia by AN
                                            mang theo giấc mơ về một thế giới thần tiên được tái hiện giữa đời
                                            thực. Đặc biệt, trong dàn mẫu nhí trình diễn có 6 em nhỏ khuyết tật,
                                            câm điếc bẩm sinh đến từ Trung tâm bảo trợ trẻ em mồ côi tàn tật TP.
                                            Việt Trì, tỉnh Phú Thọ. Các bé được tập luyện, may đo quần áo theo
                                            số đo của mình, trình diễn với các người mẫu chuyên nghiệp và nhận
                                            quà tặng của các đơn vị tài trợ, quỹ thiện nguyện, nhà hảo tâm.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/the_look_phu_tho_4f6f58fd0741409fb6f327d9348bfc2e_1024x1024.jpg')}}">
                                        </p>
                                        <p style="text-align: center;">Các bé khuyết tật, câm điếc bẩm sinh đến
                                            từ Trung tâm bảo trợ trẻ em mồ côi tàn tật TP. Việt Trì, tỉnh Phú
                                            Thọ trình diễn BST “Cổ tích” của Eunoia by AN.</p>
                                        <p>CEO Vũ Lan Anh
                                            bày tỏ: “Trước show diễn, tôi lặng lẽ quan sát những người mẹ có con
                                            là những đứa trẻ đặc biệt này “nói chuyện” với nhau. Tôi thấy con
                                            chỉ tay vào váy và tự vòng tay ôm trước ngực. Theo bản năng của một
                                            người mẹ, tôi hiểu cho hành động đó là chữ “thích”. Một chữ thích
                                            không phát ra thành lời của con gái muốn nói với mẹ về chiếc váy con
                                            đang mặc trên người. Tôi đã rơi nước mắt. Mỗi đứa trẻ đều bình đẳng
                                            về quyền sống và quyền ước mơ”.</p>
                                        <p>Chị Vũ Lan Anh cho biết những
                                            dự án thiện nguyện này chỉ là những thanh âm nhỏ bé, khiêm nhường.
                                            Tuy nhiên, chị luôn tin rằng nó có thể lan tỏa tình yêu thương, chạm
                                            tới nhiều trái tim hơn nữa để hòa thành một bản nhạc rộn ràng. Đó
                                            cũng chính là giá trị cốt lõi mà thương hiệu thời trang Eunoia by AN
                                            luôn hướng tới.</p>
