                                            ròng rã đưa đón con đi học hết các lớp tài năng này đến lớp năng
                                            khiếu khác, chị cảm nhận được sự hy sinh và vất vả của những ông bố,
                                            bà mẹ khi chứng kiến họ không quản nắng mưa, sớm tối, dù khó khăn
