                                        <p><strong>Mới đây, thương hiệu thời trang trẻ em Eunoia by AN đã
                                                trình làng BST “Black Swan”. Những thiết kế lộng lẫy, tinh tế, kiêu
                                                sa, đẹp đến từng đường nét khiến đông đảo giới mộ điệu thời trang
                                                quan tâm.</strong></p>
                                        <p>Eunoia by AN là thương hiệu thời trang trẻ
                                            em của CEO Vũ Lan Anh. Eunoia by AN (tạm dịch: ý nghĩ thuần khiết
                                            của An) ra đời giống như một món quà của chị Vũ Lan Anh dành tặng
                                            cho con gái lớn An An. Nữ CEO chia sẻ: “An An là một cô bé có năng
                                            khiếu và tư duy thẩm mỹ trong thiết kế thời trang. Chính vì thế, tôi
                                            đã quyết định mở một thương hiệu thời trang trẻ em làm nơi nuôi
                                            dưỡng cho năng khiếu và đam mê của con gái”.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/ceo_vu_lan_anh_1_-_copy_9169e37009414d4ba100df8e26a20dfb.jpg')}}">
                                        </p>
                                        <p style="text-align: center;">CEO Vũ Lan Anh là người dành nhiều tâm
                                            huyết cho thời trang trẻ em và ươm mầm tài năng nhí.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/ceo_vu_lan_anh_2_-_copy_25817653f0c243da84b4c229c6e2703b.jpg')}}">
                                        </p>
                                        <p>Với
                                            slogan “Unique as a child: Every child is a unique child and Unique
                                            as a child”, Eunoia by AN muốn gửi gắm thông điệp về sự khát khao
                                            khám phá vẻ đẹp độc đáo, cá tính riêng tiềm ẩn bên trong mỗi tâm hồn
                                            trẻ thơ.</p>
                                        <p>“Black Swan” gồm 26 mẫu thiết kế với phom dáng độc
                                            đáo và sự đầu tư kỹ lưỡng về cách xử lý chất liệu cùng kỹ thuật đính
                                            kết tỉ mỉ trong từng trang phục. Bên cạnh đó, thiết kế còn mang tới
                                            sự đột phá bởi màu sắc khi sử dụng tông màu đen ấn tượng và cuốn hút
                                            bởi vẻ huyền bí nhưng cũng không kém phần lộng lẫy, kiêu sa.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/minishow_the_charm_1_-_copy_761c02f87590442ab69ee539777279fc.jpg')}}">
                                        </p>
                                        <p style="text-align: center">BST “Black Swan” được trình làng lần đầu
                                            tại tiên tại minishow The Charm vào ngày 11/6.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/minishow_the_charm_2_-_copy_461b3c6911464e879b26ec35d7fb834d.jpg')}}">
                                        </p>
                                        <p>BST
                                            được lấy cảm hứng từ hình ảnh của cánh chim thiên nga đen cùng những
                                            chiếc lông vũ. Thiên nga đen tượng trưng cho vẻ đẹp kiêu sa cùng sự
                                            biến đổi không ngừng để tỏa sáng, vươn tới cái đẹp hoàn mỹ. Kết hợp
                                            với hình ảnh những chiếc lông vũ bay lên tựa như chắp cánh cho ước
                                            mơ bay cao và bay xa.</p>
                                        <p>Với “Black Swan”, CEO Vũ Lan Anh muốn
                                            truyền đạt đi thông điệp: “Mỗi một em bé đều là 1 phiên bản đặc biệt
                                            độc nhất vô nhị trong cuộc đời này và mỗi cô con gái sinh ra trên
                                            đời này đều là một nàng công chúa. Vậy nên tôi cũng như tất cả bà mẹ
                                            khác trên thế giới này đều mong muốn các con gái của mình hãy như
                                            một nàng thiên nga đen kiêu sa luôn cố gắng tập luyện học tập cùng
                                            sự biến đổi không ngừng để toả sáng cũng như luôn ngẩng cao đầu vươn
                                            tới cái đẹp hoàn mỹ nhất của bản thân”.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_5_58217231399d4f7e959486f758fb5173.jpg')}}">
                                        </p>
                                        <p style="text-align: center;">Dàn mẫu nhí xinh đẹp, tài năng trình
                                            diễn BST “Black Swan” tại Destination Runway Fashion Week.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_6_e63ab1c90fd340b7b0c33a08d7a8f675.jpg')}}">
                                        </p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_7_f2fe29bc2f524a198c2a7ea17138e9af.jpg')}}">
                                        </p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_8_02ae4b08218444c6a98875d114d1b744.jpg')}}">
                                        </p>
                                        <p>Trước
                                            đó, BST “Black Swan” đã từng được trình diễn lần đầu tiên vào ngày
                                            11/6 tại minishow The Charm. Dàn mẫu nhí trình diễn BST này đều là
                                            những mẫu nhí không chuyên và những mẫu nhí chưa từng bước lên sân
                                            khấu trình diễn lần nào. Như slogan của thương hiệu Eunoia by AN,
                                            CEO Vũ Lan Anh muốn định hướng phong cách trình diễn cho BST này sẽ
                                            là những điều tự nhiên, thuần khiết nhất.</p>
                                        <p>CEO Vũ Lan Anh chia
                                            sẻ: “Thời trang là làm đẹp cho đời, là tô son điểm phấn cho cuộc
                                            sống này thêm màu sắc. Khi xây dựng thương hiệu của mình, tôi muốn
                                            những mẫu thiết kế của nhãn hàng của mình sẽ đến với mọi người bất
                                            phân thân hình tiêu chuẩn hay không, bất phân độ tuổi khác nhau, chỉ
                                            cần là trình diễn trang phục của Eunoia by AN thì sẽ đều đẹp và tỏa
                                            sáng trên sàn runway”.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_9_bee3e678addd410084db1effe24174c3.jpg')}}">
                                        </p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_12_a16ac90d045e4ca88bff59219ed04ab3.jpg')}}">
                                        </p>
                                        <p>Sau
                                            sự thành công của minishow ra mắt BST “Black Swan”, CEO Vũ Lan Anh
                                            đã nhận được lời mời đồng hành cùng Xuan Lan’s Academy – đơn vị tổ
                                            chức Vietnam Junior Fashion Week tham gia Tuần lễ thời trang
                                            Destination Runway Fashion Week.</p>
                                        <p>Destination Runway Fashion
                                            Week là tuần lễ thời trang đẳng cấp do siêu mẫu Xuân Lan và Xuan
                                            Lan’s Academy tổ chức với thông điệp mang niềm tin, hy vọng đến cho
                                            trẻ em, hướng đến một tương lai tươi sáng, tốt đẹp mà ở đó mọi trẻ
                                            em đều được yêu thương và hạnh phúc. Xuất hiện trên sàn diễn của
                                            Destination Runway Fashion Week với dàn mẫu nhí trình diễn của Xuan
                                            Lan’s Academy, BST “Black Swan” lại toát lên những thần thái hết sức
                                            đặc biệt, đúng với slogan của thương hiệu là “Unique As A Child”,
                                            mỗi nàng thiên nga đen buổi tối trình diễn hôm ấy là mỗi cá tính
                                            riêng biệt, mang phong thái đầy kiêu sa, lộng lẫy.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_13_10f06d2f9cee46d483e0b0f998647f5b.jpg')}}">
                                        </p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_14_dde99236acb6438ab56f191b81c788e5.jpg')}}">
                                        </p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_15_111fc51fc90a4e049852b4e11b2d9745.jpg')}}">
                                        </p>
                                        <p>Tại
                                            Destination Runway Fashion Week, phần trình diễn BST “Black Swan” đã
                                            khiến người xem không khỏi bất ngờ và ấn tượng với hình ảnh CEO Vũ
                                            Lan Anh xách giày đi chân trần ra chào kết cùng con gái. Phải chăng
                                            CEO Vũ Lan Anh muốn gửi gắm thông điệp gì qua phần chào kết này?</p>
                                        <p>Chia
                                            sẻ về khoảnh khắc ấn tượng ấy, CEO Vũ Lan Anh cho biết: “Có một sự
                                            cố phát sinh ngoài mong muốn do lịch trình làm việc dày đặc và di
                                            chuyển liên tục với cường độ làm việc cao không ngừng nghỉ nên toàn
                                            bộ 10 đầu ngón chân của tôi đều bị sưng đỏ, gây khó khăn khi đi giày
                                            cao gót. Đến phần chào kết vì sợ ảnh hưởng đến thời gian của chương
                                            trình và gây tâm lý lo lắng cho con gái đang nắm tay mình nên tôi đã
                                            quyết định tháo giày cao gót để đi những bước chân trần – thật nhất,
                                            tự tin nhất và cười rạng ngời nhất để trấn tĩnh con gái cũng như
                                            muốn để con biết và hiểu rằng sẽ luôn có mẹ đồng hành cùng con trên
                                            mọi bước đường đi phía trước dù có khó khăn đến đâu”.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_17_18c181724a4949c0a068eca7575bb12f.jpg')}}">
                                        </p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_2_f2915ab8a7664059a7396bf08d25ce8a.jpg')}}">
                                        </p>
                                        <p>BST
                                            “Black Swan” đã nhận được sự đánh giá cao và hưởng ứng nhiệt tình
                                            của đông đảo giới mộ điệu thời trang. Góp phần tạo nên những thành
                                            công ấy, ngoài CEO Vũ Lan Anh còn có sự góp sức của đội ngũ thiết
                                            kế, stylist của Eunoia by AN. Cả ê-kíp đã cùng chị lên ý tưởng, mẫu
                                            thiết kế cùng sức sáng tạo không giới hạn và làm việc không quản
                                            ngày đêm trong nhiều tháng để cho ra đời những sản phẩm tốt nhất,
                                            gần gũi nhất cho trẻ em.</p>
                                        <p style="text-align: center"><img
                                                src="{{asset('assets/file.hstatic.net/200000685745/file/destination_runway_da_nang_4_977825e2304b4282abc9d92076afcc6c.jpg')}}">
                                        </p>
                                        <p style="text-align: center;"><em>Hình ảnh CEO Vũ Lan Anh đi chân trần
                                                ra chào kết cùng con gái gây ấn tượng mạnh mẽ với giới mộ điệu.</em></p>
                                        <p>Được
                                            biết, CEO Vũ Lan Anh tốt nghiệp Đại Học Tài Chính Thượng Hải vào năm
                                            2014, sau khi về nước chị đã quyết định lùi về phía hậu phương chăm
                                            sóc gia đình nhỏ của mình để chồng có thời gian chuyên tâm cống hiến
                                            với nghề bác sĩ. Trong thời gian đó, bên cạnh việc chăm sóc chồng
                                            con, chị còn điều hành công ty phòng khám đông y của gia đình.</p>
                                        <p>Sau
                                            8 năm, cảm nhận được sự đam mê mãnh liệt với thời trang và nghệ
                                            thuật của con gái lớn An An, cũng như những vất vả khi chứng kiến
                                            những bà mẹ không quản nắng mưa, khó khăn, vất vả vẫn cùng con theo
                                            đuổi con đường nghệ thuật mà chị đã quyết định rẽ sang ngang sang
                                            lĩnh vực nghệ thuật và truyền thông. Chị đã thành lập trung tâm học
                                            tập nghệ thuật và kỹ năng mềm Casa de Talento cùng nhãn hàng thương
                                            hiệu thời trang Eunoia by AN để là nơi ươm mầm nghệ thuật của những
                                            đứa trẻ có đam mê với thời trang và nghệ thuật như con gái của mình.</p>
