                                            liên tục với mong muốn đem đến những màn trình diễn đẹp mắt, truyền
                                            thông điệp kết nối, trao yêu thương đến cộng đồng. Cụ thể, các mẫu
                                            nhí sẽ đóng góp một ngày lương, tặng áo mới cho các bạn nhỏ vùng
                                            cao.</p>
