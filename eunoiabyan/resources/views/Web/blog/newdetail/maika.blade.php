                                            tích” được lựa chọn để kết thúc chương trình với thông điệp mở ra
                                            một thế giới với bao khát vọng tuổi thơ, Maika đã vô cùng tự tin khi
                                            đứng bên một diễn viên, ca sĩ dày dặn kinh nghiệm để cùng truyền tải
                                            những lời ca đầy ý nghĩa. Màn biểu diễn của công chúa Maika cùng
                                            “hoàng tử” Bùi Minh Quân thực sự để lại nhiều xúc cảm trong lòng
                                            khán giả.</p>
                                        <p>Nguyễn Hoàng Ngọc Khánh sinh năm 2012 tại Hà Nội. Dù
                                            còn nhỏ tuổi nhưng con đã đạt được rất nhiều danh hiệu: Cover Model
                                            của trang bìa tạp chí Queen and King Fashion, tham gia tuần lễ thời
                                            trang quốc tế như Aquafina International Fashion Week 2022, là 1
                                            trong 2 mẫu nhí tham dự Festival Quảng Ninh – Miền Di Sản – quảng bá
                                            du lịch Việt Nam 2022.</p>
