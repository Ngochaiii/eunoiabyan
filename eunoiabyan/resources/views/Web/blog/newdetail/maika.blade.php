                                            nhận được sự chú ý của đông đảo người hâm mộ thời trang. Kết thúc
                                            chương trình là những mẫu váy công chúa trên sân khấu lung linh sắc
                                            màu cổ tích, nàng công chúa Maika (nghệ danh của mẫu nhí Nguyễn
                                            Hoàng Ngọc Khánh) đã có một màn biểu diễn thật sự ấn tượng. Không
                                            chỉ thể hiện vai trò kết màn cho bộ sưu tập “Cổ tích” vô cùng xuất
                                            sắc với từng bước đi uyển chuyển, gương mặt biểu cảm, Maika còn thử
                                            sức với khả năng “diễn xuất” khi hoá thân thành một nàng công chúa
                                            hoà mình vào lời ca, tiếng hát đẹp đẽ của ca khúc “A Whole New
                                            World”.</p>
                                        <p style="text-align: center"><img
